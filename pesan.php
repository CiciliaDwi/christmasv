    <!-- Script untuk kontrol audio -->
    <script>
        const audio = document.getElementById('christmasSong');

        // Ambil posisi terakhir lagu dari localStorage saat halaman dimuat
        window.onload = function () {
            const savedTime = localStorage.getItem('audioCurrentTime');
            if (savedTime) {
                audio.currentTime = parseFloat(savedTime);
            }

            if (localStorage.getItem('audioStatus') === 'playing') {
                audio.play();
            } else if (localStorage.getItem('audioStatus') === 'paused') {
                audio.pause();
            }
        };

        // Simpan posisi lagu sebelum meninggalkan halaman
        window.onbeforeunload = function () {
            localStorage.setItem('audioCurrentTime', audio.currentTime);
        };


        function playAudio() {
            audio.play();
            localStorage.setItem('audioStatus', 'playing');
        }

        function pauseAudio() {
            audio.pause();
            localStorage.setItem('audioStatus', 'paused');
        }
    </script>
